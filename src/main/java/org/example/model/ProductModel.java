package org.example.model;

public class ProductModel {
    // Attributes
    private int id;
    private String name;
    private double price;
    private int stock;

    // Constructor
    public ProductModel(){}

    // Setter & getters
    public void setId(int id) {
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public double getPrice() {
        return price;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }

    public int getStock() {
        return stock;
    }

}
